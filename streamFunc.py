def makeIndent(txt):
    lines = txt.split('\n')
    indent = '    '
    childCnt = 0
    familyCnt = 0
    global loopCnt

    while loopCnt <= len(lines):
        for i in range(loopCnt, len(lines)+1):
            if '<A' in lines[-i]:
                childCnt += 1 
                familyCnt +=1
                loopCnt +=1
            else:
                if lines[-i].split(',')[1] == ' n':
                    lines[-i] = lines[-i].replace('n','1')
                L_len = int(lines[-i].split(',')[1]) # <L, n to <L, 1
                if i == len(lines): 
                    for j in range(1, len(lines)):
                        lines[j] = lines[j].replace('<', indent+'<')
                    lines[-1] += '>'
                    return '\n'.join(lines) # end

                # children qty == <L, len>
                if L_len == childCnt:
                    for j in range(i-1, i-familyCnt-1, -1):
                        lines[-j]= lines[-j].replace('<', indent+'<')
                    lines[-i+familyCnt] += '>'
                    familyCnt += 1
                    loopCnt += 1
                    childCnt = 1

                elif L_len > childCnt: 
                    for j in range(i-1, 0, -1):
                        lines[-j] = lines[-j].replace('<', indent+'<')
                else:
                    loopCnt -= (childCnt-1)  # childCnt > L_len
                    break

        childCnt = 0
        familyCnt = 0
    return '\n'.join(lines)

